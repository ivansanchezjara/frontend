/**
 * Retorna todas las ciudades de todos los departamentos en una lista plana.
 */
export function getAllCiudades() {
  const all = [];
  for (const ciudades of Object.values(CIUDADES_POR_DEPARTAMENTO)) {
    all.push(...ciudades);
  }
  return [...new Set(all)].sort((a, b) => a.localeCompare(b, "es"));
}
